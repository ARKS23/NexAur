    void ContainerPass::execute() {
        float delta_time = m_clock.getElapsedTime();

        glm::mat4 transform = glm::mat4(1.0f);
        transform = glm::translate(transform, glm::vec3(2.0f, 3.0f, -5.0f));
        transform = glm::rotate(transform, glm::radians(30.0f * delta_time), glm::vec3(1.0f, 0.3f, 0.5f));
        transform = glm::scale(transform, glm::vec3(1.5f));
        Renderer::submit(m_material, m_vertex_array, transform);
    }
